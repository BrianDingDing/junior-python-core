# 1. +: 拼接 *: 重复
print("悟空" + "八戒")
print("悟空" * 2)

# 2. == != > <
# 依次比较两个容器中元素, 一但不同则返回比较结果.
# 容器元素比较, 首先1和1编码值比较, 然后1和5编码值比较.
print("11w" > "15w")  # False
print("11" > "2")  # False
# 只是1和0比较, 一但不同则返回比较结果.
print("11" > "02")  # True

# 3. in / not in
print("美猴王" in "我是花果山水帘洞美猴王.")  # True
print("美王" in "我是花果山水帘洞美猴王.")  # False
print("王猴" in "我是花果山水帘洞美猴王.")  # False
